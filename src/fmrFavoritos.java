    private void btnQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarActionPerformed
        try {
            int fila = tblFavoritos.getSelectedRow();
            if (fila == -1) {
                JOptionPane.showMessageDialog(this, "Selecciona un libro.");
                return;
            }

            int id = Integer.parseInt(tblFavoritos.getValueAt(fila, 0).toString());

            service.marcarFavorito(id, false);
            cargarTablaFavoritos();

            JOptionPane.showMessageDialog(this, "Libro quitado de favoritos.");

        } catch (Exception e) {
            mostrarError(e);
        } 
    }//GEN-LAST:event_btnQuitarActionPerformed

