    private void configurarTabla() {
        modelo = new DefaultTableModel(
            new Object[]{"ID", "Titulo", "AÃ±o"},
            0
        ) {
            @Override public boolean isCellEditable(int r, int c) { return false; }
        };

        tblFavoritos.setModel(modelo);
    }
    
    private void cargarTablaFavoritos() {
        try {
            List<Libro> lista = service.listarFavoritos();
            DefaultTableModel m = (DefaultTableModel) tblFavoritos.getModel();
            m.setRowCount(0);

            for (Libro l : lista) {
                m.addRow(new Object[]{
                    l.getId(),
                    l.getTitulo(),
                    l.getAnio()
                });
            }
        } catch (Exception e) {
            mostrarError(e);
        }
    }
    
    private void mostrarError(Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

